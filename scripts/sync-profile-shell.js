/**
 * Synchronizes static public shell files from the FAB app profile. Run this
 * when profile-owned document title, description, app name, or noscript copy
 * changes so the checked-in HTML and manifest match runtime metadata.
 */
import fs from "fs/promises";
import path from "path";
import { fileURLToPath } from "url";

import { APP_PROFILE } from "../src/features/fab/profile.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT_DIR = path.join(__dirname, "..");
const PUBLIC_DIR = path.join(ROOT_DIR, "public");

const shellConfig = APP_PROFILE.shell || {};
const brandConfig = APP_PROFILE.brand || {};

const documentTitle = shellConfig.documentTitle || brandConfig.appName || "App";
const appDescription = shellConfig.description || "";
const noScriptMessage = shellConfig.noScriptMessage || "You need to enable JavaScript to run this app.";
const manifestName = shellConfig.manifestName || documentTitle;
const manifestShortName = shellConfig.manifestShortName || manifestName;

const escapeHtml = (value = "") => (
  String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll("\"", "&quot;")
    .replaceAll("'", "&#39;")
);

// Public shell files are generated from templates so marketing metadata, PWA
// names, and noscript copy stay aligned with the FAB app profile.
const writeIndexHtml = async () => {
  const templatePath = path.join(PUBLIC_DIR, "index.template.html");
  const outputPath = path.join(PUBLIC_DIR, "index.html");

  let template = await fs.readFile(templatePath, "utf8");
  const replacements = new Map([
    ["__APP_DESCRIPTION__", escapeHtml(appDescription)],
    ["__APP_TITLE__", escapeHtml(documentTitle)],
    ["__APP_NOSCRIPT_MESSAGE__", escapeHtml(noScriptMessage)],
  ]);

  for (const [token, value] of replacements.entries()) {
    template = template.replaceAll(token, value);
  }

  const output = `<!-- Generated by bun run sync:profile-shell. Edit public/index.template.html or src/features/fab/profile.js instead. -->\n${template}`;
  await fs.writeFile(outputPath, output);
};

const writeManifest = async () => {
  const templatePath = path.join(PUBLIC_DIR, "manifest.template.json");
  const outputPath = path.join(PUBLIC_DIR, "manifest.json");

  const manifest = JSON.parse(await fs.readFile(templatePath, "utf8"));
  // Keep only the profile-owned fields dynamic; icons, display mode, and theme
  // metadata continue to live in the checked-in manifest template.
  manifest.short_name = manifestShortName;
  manifest.name = manifestName;

  await fs.writeFile(outputPath, `${JSON.stringify(manifest, null, 2)}\n`);
};

const main = async () => {
  await Promise.all([writeIndexHtml(), writeManifest()]);
  console.log("Synced public shell metadata from the FAB app profile.");
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
